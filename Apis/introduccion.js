/* Que son las API?
Application Program}ming Interface*/

/*APIs del navegador web
Los navegadores nos dan acceso a diferentes API con las que podemos:
-Local Storage API - Trabajar almacenamiento local
-Fetch API - Hacer peticiones a servidores.
-History API - Para poder acceder al historial de la pagina.
-Validation API - Para hacer uso de las funciones del navegador para validar formularios.*/

/*APIs externas
Tambien puede haber APIs externas que nos permitan acceder a recursos de otra pagina web.
Ejemplo con la API de youtube:
-Nos permite obtener informacion de video y canales.
-Nos permite subir videos.

Estas son cosas que no podriamos hacer desde nuestra pagina si no existiera una API.
Gracias aque existe una API, podemos hablarle y pedirle obtener datos y hacer operaciones.*/